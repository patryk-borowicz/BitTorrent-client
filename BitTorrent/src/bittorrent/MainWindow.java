    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        FileDialog fd = new FileDialog(this, "Choose a file", FileDialog.LOAD);
        fd.setDirectory("C:\\");
        fd.setFile("*.torrent");
        fd.setVisible(true);

        String filename = fd.getFile();

        if (filename != null) {
            switch (client.loadTorrent(fd.getDirectory() + "\\" + filename,
                    (DefaultTableModel) torrentsTable.getModel())) {
                case OK:
                    torrentsTable.setRowSelectionInterval(0, torrentsTable.getRowCount() - 1);

                    break;
                case FAIL:
                    JOptionPane.showMessageDialog(this,
                            "Torrent load fail!",
                            "Error - " + WINDOW_TITLE,
                            JOptionPane.ERROR_MESSAGE);
                    break;
                case EXIST:
                    JOptionPane.showMessageDialog(this,
                            "Torrent already exist!",
                            "Error - " + WINDOW_TITLE,
                            JOptionPane.ERROR_MESSAGE);
                    break;
                default:
                    throw new AssertionError(client.loadTorrent(filename, (DefaultTableModel) torrentsTable.getModel()).name());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

