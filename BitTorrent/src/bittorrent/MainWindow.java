    public MainWindow() {
        initComponents();
        setTitle(WINDOW_TITLE);
        String pathToImageSortBy = "icons/hex_block.png";
        ImageIcon img = new ImageIcon(getClass().getClassLoader().getResource(pathToImageSortBy));
        setIconImage(img.getImage());
        
        client = new Client();
        client.refreshButtons(torrentsTable, startButton, stopButton, jProgressBar1);
        setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
        JFrame frame = this;
        addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosing(java.awt.event.WindowEvent windowEvent) {
                if (JOptionPane.showConfirmDialog(frame,
                        "Are you sure?", "Exit - " + WINDOW_TITLE,
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
                    System.exit(0);
                }
            }
        });
        
        client.refreshTorrents(torrentsTable);
        PiecesPanel panel = new PiecesPanel(null, 0, getWidth());
        jTabbedPane1.add(panel, "Pieces");

        new Thread() {
            @Override
            public void run() {
                while (true) {
                    if (torrentsTable.getSelectedRow() > -1) {
                        String sha1 = torrentsTable.getValueAt(torrentsTable.getSelectedRow(), 5).toString();
                        client.loadPeersTrackerTable(sha1, 
                                (DefaultTableModel) jTable2.getModel(),
                                (DefaultTableModel) jTable3.getModel(),
                                panel, getWidth());
                        client.loadFilesTable(sha1, (DefaultTableModel) jTable1.getModel());
                         jTabbedPane1.revalidate();
                        jTabbedPane1.repaint();
                    }
                    try {
                        Thread.sleep(500);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }.start();
    }

